// Helper function to get color by mode
export const getColor = (
  colorName: keyof typeof colors,
  mode: ColorThemeMode = 'light',
): string => {
  return colors[colorName][mode];
};

// Helper to get all colors for a specified mode
export const getThemeColors = (
  mode: ColorThemeMode = 'light',
): Record<string, string> => {
  const themeColors: Record<string, string> = {};

  Object.entries(colors).forEach(([colorName, modeValues]) => {
    themeColors[colorName] = modeValues[mode];
  });

  return themeColors;
};
